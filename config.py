# =============================================================================
# Тип отображения координат точек, фотонов и горизонтов:
#   "physical" — физические (proper) координаты: точки расходятся вместе с расширением,
#                физическая линейка постоянной длины, комовинг-линейка ∝ a(t).
#   "comoving" — сопутствующие координаты: точки на фиксированных χ-позициях, горизонты
